num1 = int (input("Please enter your first number"))
operator = input("Please enter your operator")
num2 = int (input("Please enter your second number"))

if operator == "+":
    print (num1+num2)


if  operator == "-":
    print (num1-num2)

if operator =="*":
    print(num1*num2)

if operator =="/":
    print(num1/num2)


